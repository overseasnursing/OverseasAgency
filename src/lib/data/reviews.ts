export function getReviewsByAgency(agencySlug: string): PlatformReview[] {
  return PLATFORM_REVIEWS.filter((r) => r.agencySlug === agencySlug)
}

export function getFeaturedReviews(): PlatformReview[] {
  return PLATFORM_REVIEWS.filter((r) => r.featured)
}

export function getAllReviews(): PlatformReview[] {
  return PLATFORM_REVIEWS
}

export function getReviewsByCountry(country: string): PlatformReview[] {
  return PLATFORM_REVIEWS.filter(
    (r) => r.destinationCountry.toLowerCase() === country.toLowerCase()
  )
}

export function getReviewStats() {
  const total = PLATFORM_REVIEWS.length
  const placed = PLATFORM_REVIEWS.filter((r) => r.verifiedPlacement).length
  const withHiddenCharges = PLATFORM_REVIEWS.filter((r) => r.hiddenChargesExperienced).length
  const avgRating =
    PLATFORM_REVIEWS.reduce((sum, r) => sum + r.rating, 0) / total
  const wouldRecommend = PLATFORM_REVIEWS.filter((r) => r.wouldRecommend).length

  return {
    total,
    placed,
    withHiddenCharges,
    avgRating: Math.round(avgRating * 10) / 10,
    recommendPercent: Math.round((wouldRecommend / total) * 100),
  }
}
